fun main()
{
  // Arithmetic Operations
  var items = 10

  // Reassign
  items = 8

  // Addition
  items = items + 4
  items += 4

  // Subtraction
  items = items - 4
  items -= 4

  // Multiplication
  items = items * 4
  items *= 4

  // Division
  items = items / 10
  items /= 10

  println(items) // 1

  // basic types
  // integers (Byte, Short, Int, Long)
  // Unsigned integers (UByte, UShort, UInt, Ulong)
  // Floating-point numbers (Float, Double)
  // Booleans (Boolean)
  // Characters (Char)
  // Strings (String)

  // Declare Variable without initialization
  val a: Int

  // Variable Initialized.
  a = 3

  // Variable Explicitly typed and initialized
  val e: String = "Hello"

  // Variables can be read because they have been initialized.
  println(a) // 3
  println(e) // Hello
}